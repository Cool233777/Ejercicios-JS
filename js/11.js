//Iteradores en JS

const tecnologias = ['HTML', 'CSS', 'C#', 'JavaScript', 'Node.js']

//foreach - solo lista accede a cada elemento
const arrayForeach = tecnologias.forEach(function(item){
    return item
})

//map - crea un nuevo array
const arrayMap = tecnologias.map(function(item){
    return item
})

console.log(arrayForeach)
console.log(arrayMap)
