//Condicionales

const disponible = 4000
const retirar = 200

if (disponible > retirar) {
    //se cumple la condicion
    console.log('Si puedes retirar')
    
}else {
    //no se cumple la condicion
    console.log('No puedes retirar')
}  


/*
    > - mayor que
    < - menor que
    >= - mayor o igual que
    <= - menor o igual que
*/